            <h3>El ayuno intermitente:</h3>
            <p>El ayuno intermitente es una estrategia nutricional que consiste en alternar períodos de ayuno con períodos de alimentación. Algunos estudios sugieren que el ayuno intermitente puede tener beneficios para la salud, como la pérdida de peso, la mejora de la sensibilidad a la insulina y la reducción del riesgo de enfermedades crónicas.</p>   
        </div>
        <div class="dietas-contenedor">
            <picture>
                <source loading="lazy" srcset="build/img/dietacetogenica-fond.webp" type="image/webp">
                <img loading="lazy" src="build/img/dietacetogenica-fond.jpg" alt="Imagen-cetógenica">
            </picture>
            <h3>La dieta cetogénica:</h3>
            <p>La dieta cetogénica es una dieta baja en carbohidratos y alta en grasas que se ha popularizado en los últimos años como una forma efectiva de perder peso. La dieta cetogénica se basa en la cetosis, un estado metabólico en el que el cuerpo utiliza la grasa como fuente de energía en lugar de los carbohidratos.</p>
        </div>
        <div class="dietas-contenedor">
            <picture>
                <source loading="lazy" srcset="build/img/dietamediterranea-fond.webp" type="image/webp">
                <img loading="lazy" src="build/img/dietamediterranea-fond.jpg" alt="Imagen-dieta-mediterránea">
            </picture>
            <h3>La dieta mediterránea:</h3>
            <p>La dieta mediterránea es un patrón alimentario tradicional de los países mediterráneos que se caracteriza por un alto consumo de frutas, verduras, legumbres, frutos secos, pescado y aceite de oliva, y un bajo consumo de carne roja, productos lácteos y alimentos procesados. La dieta mediterránea se ha asociado con numerosos beneficios para la salud, como la reducción del riesgo de enfermedades crónicas y la longevidad.</p>
        </div>
        <div class="dietas-contenedor">
            <picture>
                <source loading="lazy" srcset="build/img/dietavegetariana-fond.webp" type="image/webp">
                <img loading="lazy" src="build/img/dietavegetariana-fond.jpg" alt="Imagen-dieta-vegetariana">
            </picture>
            <h3>La dieta vegetariana:</h3>
            <p>La dieta vegetariana es una dieta que excluye la carne y el pescado y que se basa en el consumo de alimentos de origen vegetal, como frutas, verduras, legumbres, frutos secos, semillas y cereales. La dieta vegetariana se ha asociado con numerosos beneficios para la salud, como la reducción del riesgo de enfermedades crónicas, la pérdida de peso y la protección del medio ambiente.</p>
        </div>
    </div>
