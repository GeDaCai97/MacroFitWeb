<section class="contenedor-macronutri">
    <div class="macronutri-contenido">
        <div class="macronutri-concepto" id="macronutrientes">
            <h3>Los Macronutrientes:</h3>
            <p>Tienes tres grupos: glúcidos o carbohidratos, proteínas y lípidos o grasas. Todos ellos están presentes en mayor o menor medida en 
                diferentes alimentos y cada uno de ellos es necesario para conseguir la energía que necesitamos. Los carbohidratos se almacenan como 
                glucógeno en tus músculos y te dan la energía necesaria para la actividad física; las proteínas, además de muchas otras importantísimas 
                funciones, son el andamio del tejido muscular y, por tanto, juegan un papel importante para aumentarlo o reducirlo; y, por último, las 
                grasas que, a pesar de su mala prensa, son muy necesarias para el correcto funcionamiento del cuerpo.
            </p>
        </div>
        
        <div class="macronutri-concepto">
            <h3>Los Micronutrientes:</h3>
            <p>
            Los micronutrientes son las vitaminas y los minerales. Aunque no aportan energía, son fundamentales para el correcto funcionamiento del cuerpo.
            Las vitaminas son necesarias para que se produzcan las reacciones químicas que permiten la vida y los minerales son necesarios para la 
            formación de huesos, dientes, sangre y para mantener el equilibrio de los líquidos corporales. Por tanto, es fundamental que tu dieta 
            sea variada y equilibrada para que no te falte ninguno de ellos.
            </p>
        </div>
        <div class="macronutri-concepto">
            <h3>Porcentajes recomendados de cada grupo:</h3>
            <p>
            Tal vez conocías los macronutrientes, pero quizá no sepas qué porcentaje es el recomendable de cada grupo. El más estable es el 
            de las grasas, un 20% o 25% de la ingesta de calorías del que la mayor parte debe proceder de grasas no saturadas y tan sólo un 10% o menos de 
            las saturadas. En el caso de los carbohidratos, deben suponer un 50% de las calorías que ingieres al día y, por último, las proteínas deben 
            estar en torno al 30%. Obviamente, esto son cifras generales, si quieres ganar músculo debes aumentar el consumo de proteínas que tienen que 
            llegar a ser hasta 1,5 gramos por kilo de peso o incluso un poco más. También tendrás que elevar la cantidad de carbohidratos para que tus 
            músculos aguanten los entrenamientos.
            </p>
        </div>  
    </div>
</section>   
<section class="seccion-conceptos">
    <div class="concepto c-agua">
        <div class="contenedor-conceptos">
            <h3>El Agua:</h3>
            <p>El agua es el nutriente más importante para el ser humano. El cuerpo humano está compuesto por un 60% de agua y es fundamental para la vida. El agua es necesaria para la digestión, la absorción y el transporte de los nutrientes, para la eliminación de los residuos del metabolismo y para regular la temperatura corporal. Por tanto, es fundamental que te hidrates correctamente, bebiendo al menos 2 litros de agua al día.</p>
        </div>
    </div>
    <div class="concepto c-fibra">
        <div class="contenedor-conceptos">
            <h3>La Fibra:</h3>
            <p>La fibra es un tipo de carbohidrato que no se digiere y que es fundamental para el correcto funcionamiento del sistema digestivo. La fibra ayuda a prevenir el estreñimiento, a controlar el azúcar en sangre y a reducir el colesterol. Por tanto, es fundamental que consumas alimentos ricos en fibra, como frutas, verduras, legumbres y cereales integrales.</p>
        </div>
    </div>
    <div class="concepto c-antioxidantes">
        <div class="contenedor-conceptos">
            <h3>Los Antioxidantes:</h3>
            <p>Los antioxidantes son sustancias que protegen a las células del daño causado por los radicales libres, que son moléculas inestables que pueden dañar las células y contribuir al envejecimiento y a la aparición de enfermedades. Los antioxidantes se encuentran en alimentos como frutas, verduras, frutos secos, semillas y té verde.</p>
        </div>
    </div>
    <div class="concepto c-alimentos">
        <div class="contenedor-conceptos">    
            <h3>Los Superalimentos:</h3>
            <p>Los superalimentos son alimentos que tienen un alto contenido de nutrientes y que se consideran beneficiosos para la salud. Algunos ejemplos de superalimentos son la quinoa, el aguacate, las bayas, el brócoli, el salmón y el té verde.</p>
        </div>
    </div>
    
</section>
    
<section class="seccion-dietas" id="tiposdietas">
    <h2>Tipos de Dietas</h2>
    <div class="tipos-dietas">
        <div class="dietas-contenedor">
