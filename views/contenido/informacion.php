        <h2>Tipos de Rutinas de Ejercicio para la Pérdida de Peso</h2>
        <p>Aquí encontrarás una variedad de ejercicios y rutinas de entrenamiento diseñadas específicamente para ayudarte a perder peso. Recuerda, la clave para perder peso es quemar más calorías de las que consumes.</p>
        <div class="rutinas-flex">
            <div class="rutinas-view">
                <p>1. Cardio: Correr, nadar, andar en bicicleta son excelentes formas de quemar calorías.</p>
            </div>
            <div class="rutinas-view">
                <p>2. Entrenamiento de fuerza: Levantamiento de pesas, yoga, pilates pueden ayudarte a construir músculo, lo que puede aumentar tu metabolismo y ayudarte a quemar más calorías.</p>
            </div>
            <div class="rutinas-view">
                <p>3. Ejercicios de alta intensidad: Los entrenamientos de intervalos de alta intensidad (HIIT) son una excelente manera de quemar calorías rápidamente.</p>
            </div>
            <div class="rutinas-view">
                <p>4. Entrenamiento de circuito: El entrenamiento de circuito es una forma efectiva de combinar el cardio y el entrenamiento de fuerza en una sola rutina.</p>
            </div>
            <div class="rutinas-view">
                <p>5. Entrenamiento en casa: Si no tienes acceso a un gimnasio, hay muchas rutinas de entrenamiento que puedes hacer en casa con poco o ningún equipo.</p>
            </div>
        </div>
        <p>Recuerda, la clave para perder peso es la consistencia y la paciencia. No esperes ver resultados de la noche a la mañana, pero si te mantienes comprometido y sigues trabajando duro, verás los resultados que deseas.</p>   
